var phantom = document.getElementById("phantom");
var phantomImg = document.getElementById("phantom_image");
var phantomText = document.getElementById("phantom_text");

phantom.onmouseover = function(){
    phantomImg.style.display = "none";
    phantom.style.backgroundColor = "#ffef29";
    phantomText.style.display = "block";
}
phantom.onmouseleave = function(){
    phantomText.style.display = "none";
    phantom.style.backgroundColor = "transparent";
    phantomImg.style.display = "inline";
}
phantom.onclick = function(){
    location.href = "https://phntm.co";
}

